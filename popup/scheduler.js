/**
 * Calculates the booking window open date (classDate minus daysAhead).
 * Returns MM/DD/YYYY for schtasks compatibility.
 * @param {number} daysAhead
 * @param {string|null} classDate - YYYY-MM-DD from wizard
 * @returns {string} MM/DD/YYYY
 */
function calcScheduleDate(daysAhead, classDate) {
  let d;
  if (classDate) {
    const [yyyy, mm, dd] = classDate.split('-').map(Number);
    d = new Date(yyyy, mm - 1, dd);     // local midnight ‚Äî no UTC shift
    d.setDate(d.getDate() - daysAhead); // go back daysAhead days
  } else {
    d = new Date(); // fallback: today
  }
  const mo  = String(d.getMonth() + 1).padStart(2, '0');
  const day = String(d.getDate()).padStart(2, '0');
  const yr  = d.getFullYear();
  return `${mo}/${day}/${yr}`;
}

/**
 * Returns the day-of-week name from a YYYY-MM-DD date string.
 * Uses local date constructor to avoid UTC timezone shifting.
 * e.g. "2026-06-03" ‚Üí "Wednesday"
 * @param {string|null} classDate
 * @returns {string}
 */
function getDayOfWeek(classDate) {
  if (!classDate) return 'Monday';
  const [yyyy, mm, dd] = classDate.split('-').map(Number);
  const days = ['Sunday','Monday','Tuesday','Wednesday','Thursday','Friday','Saturday'];
  return days[new Date(yyyy, mm - 1, dd).getDay()];
}

/**
 * Parses a time string like "9:00 AM" or "09:00" into 24-hour HH:MM.
 * @param {string} timeStr
 * @returns {string} HH:MM
 */
function parseTo24Hour(timeStr) {
  const clean = timeStr.trim().toUpperCase();
  const ampm  = clean.includes('AM') ? 'AM' : clean.includes('PM') ? 'PM' : null;
  const parts = clean.replace('AM', '').replace('PM', '').trim().split(':');
  let hours   = parseInt(parts[0], 10);
  const mins  = parts[1] ? parts[1].padStart(2, '0') : '00';
  if (ampm === 'PM' && hours !== 12) hours += 12;
  if (ampm === 'AM' && hours === 12) hours = 0;
  return `${String(hours).padStart(2, '0')}:${mins}`;
}

/**
 * Generates RegisterTask.bat ‚Äî registers a weekly Windows Scheduled Task
 * AND configures all required system settings automatically.
 * User right-clicks and runs as Administrator once.
 * @param {object} config - wizard state.config or state.config.schedule
 * @param {string} macroName - e.g. "BookingTemplate"
 * @returns {string} bat file content
 */
function generateRegisterTaskBat(config, macroName) {
  const classDate  = config.classDate  || (config.schedule && config.schedule.classDate)  || null;
  const daysAhead  = Number(config.daysAhead  || (config.schedule && config.schedule.daysAhead)  || 7);
  const runTime24  = config.runTime  || (config.schedule && config.schedule.runTime)  || '09:00';

  const schedDate     = calcScheduleDate(daysAhead, classDate);
  const taskName      = `LifeTimeBooking_${macroName.replace(/\s+/g, '_')}`;
  const [mo, day, yr] = schedDate.split('/');
  const isoDateTime   = `${yr}-${mo}-${day}T${runTime24}:00`;
  const dayOfWeek     = getDayOfWeek(classDate);

  return `@echo off
REM ============================================================
REM  RegisterTask.bat ‚Äî System Setup + Weekly Scheduled Task
REM  RIGHT-CLICK and select "Run as administrator"
REM  You only need to do this ONCE.
REM ============================================================

SET TASK_NAME=${taskName}
SET BAT_PATH=%~dp0RunBooking.bat
SET XML_PATH=%~dp0task.xml

echo(
echo ============================================================
echo  STEP 1 ‚Äî Configuring system settings for reliable automation
echo ============================================================
echo(

REM Enable wake timers on AC power and battery
powercfg /setacvalueindex SCHEME_CURRENT SUB_SLEEP RTCWAKE 1
powercfg /setdcvalueindex SCHEME_CURRENT SUB_SLEEP RTCWAKE 1
powercfg /setactive SCHEME_CURRENT
echo [OK] Wake timers enabled

REM Set sleep timeout to 30 minutes so PC stays awake during macro
powercfg /change standby-timeout-ac 30
powercfg /change standby-timeout-dc 30
echo [OK] Sleep timeout set to 30 minutes minimum

REM Disable fast startup (can interfere with scheduled wake)
reg add "HKLM\\SYSTEM\\CurrentControlSet\\Control\\Session Manager\\Power" /v HiberbootEnabled /t REG_DWORD /d 0 /f >nul 2>&1
echo [OK] Fast startup disabled

REM Allow UIVision extension to access local file URLs
reg add "HKLM\\SOFTWARE\\Policies\\Google\\Chrome\\ExtensionSettings\\gcbalfbdmfieckjlnblleoemohcganoc" /v "file_url_navigation_allowed" /t REG_DWORD /d 1 /f >nul 2>&1
echo [OK] UIVision file access configured

REM Check if UIVision storage folder exists (confirms UIVision is installed)
SET UIVISION_DIR=%USERPROFILE%\\AppData\\Local\\Google\\Chrome\\User Data\\Default\\File System
IF EXIST "%UIVISION_DIR%" (
  echo [OK] UIVision installation detected
) ELSE (
  echo [!!] WARNING: UIVision folder not found.
  echo      Make sure UIVision is installed in Chrome before running the macro.
  echo      Install from: https://chrome.google.com/webstore/detail/uivision/gcbalfbdmfieckjlnblleoemohcganoc
)

REM Warn if domain-joined (corporate PCs may force screen lock)
for /f "tokens=2 delims==" %%a in ('wmic computersystem get partofdomain /value 2^>nul') do set DOMAIN=%%a
if /i "%DOMAIN%"=="TRUE" (
  echo(
  echo [!!] WARNING: This PC is joined to a corporate domain.
  echo      Your IT policy may lock the screen and block the macro.
  echo      Contact your IT department if bookings fail silently.
)

echo(
echo ============================================================
echo  STEP 2 ‚Äî Registering weekly scheduled task
echo ============================================================
echo(
echo Task name   : %TASK_NAME%
echo First run   : ${isoDateTime}
echo Repeats     : Every ${dayOfWeek} at ${runTime24}
echo Bat file    : %BAT_PATH%
echo(

REM Write the task XML file
(
echo ^<?xml version="1.0" encoding="UTF-16"?^>
echo ^<Task version="1.2" xmlns="http://schemas.microsoft.com/windows/2004/02/mit/task"^>
echo   ^<Triggers^>
echo     ^<CalendarTrigger^>
echo       ^<StartBoundary^>${isoDateTime}^</StartBoundary^>
echo       ^<Enabled^>true^</Enabled^>
echo       ^<ScheduleByWeek^>
echo         ^<WeeksInterval^>1^</WeeksInterval^>
echo         ^<DaysOfWeek^>
echo           ^<${dayOfWeek}/^>
echo         ^</DaysOfWeek^>
echo       ^</ScheduleByWeek^>
echo     ^</CalendarTrigger^>
echo   ^</Triggers^>
echo   ^<Settings^>
echo     ^<StartWhenAvailable^>true^</StartWhenAvailable^>
echo     ^<WakeToRun^>true^</WakeToRun^>
echo     ^<DisallowStartIfOnBatteries^>false^</DisallowStartIfOnBatteries^>
echo     ^<StopIfGoingOnBatteries^>false^</StopIfGoingOnBatteries^>
echo     ^<ExecutionTimeLimit^>PT1H^</ExecutionTimeLimit^>
echo     ^<MultipleInstancesPolicy^>IgnoreNew^</MultipleInstancesPolicy^>
echo   ^</Settings^>
echo   ^<Actions Context="Author"^>
echo     ^<Exec^>
echo       ^<Command^>"%BAT_PATH%"^</Command^>
echo     ^</Exec^>
echo   ^</Actions^>
echo   ^<Principals^>
echo     ^<Principal id="Author"^>
echo       ^<LogonType^>InteractiveToken^</LogonType^>
echo       ^<RunLevel^>HighestAvailable^</RunLevel^>
echo     ^</Principal^>
echo   ^</Principals^>
echo ^</Task^>
) > "%XML_PATH%"

schtasks /create /tn "%TASK_NAME%" /xml "%XML_PATH%" /f

IF %ERRORLEVEL% EQU 0 (
  echo(
  echo ============================================================
  echo  SUCCESS! Everything is configured and scheduled.
  echo ============================================================
  echo(
  echo Task      : %TASK_NAME%
  echo First run : ${isoDateTime}
  echo Repeats   : Every ${dayOfWeek} at ${runTime24}
  echo(
  echo To run RIGHT NOW for testing, double-click RunBooking.bat
  echo(
  echo IMPORTANT: PC must be ON or in SLEEP mode at booking time.
  echo            Hibernate and Shutdown will prevent the wake.
  echo(
  echo If wake-from-sleep does not work, check Windows power settings:
  echo   Settings - System - Power and Sleep - Additional power settings
  echo   Change plan settings - Change advanced power settings
  echo   Sleep - Allow wake timers - set to Enabled
) ELSE (
  echo(
  echo ERROR: Scheduled task could not be registered.
  echo Did you right-click and choose "Run as administrator"?
)

REM Clean up temp XML
IF EXIST "%XML_PATH%" DEL "%XML_PATH%"

echo(
pause
`;
}

